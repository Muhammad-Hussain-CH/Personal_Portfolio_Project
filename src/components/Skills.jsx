        ctx.strokeStyle = isFront ? skill.color + (isFront ? 'BB' : '30') : skill.color + '22'
        ctx.lineWidth = isFront ? 1.4 * scale : 0.6
        ctx.stroke()

        // icon + text layout
        const startX = sx - contentW / 2

        // draw icon
